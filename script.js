function clicar(num) {
    if (num === 1) {
        window.alert('Você clicou no 1º Botão!')
    } else if (num === 2) {
        window.alert('Você clicou no 2º Botão!')
    } else if (num === 3) {
        window.alert('Você clicou no 3º Botão!')
    } else {
        window.alert('[ERRO] Houve algo erro desconhecido! :(')
    }
}